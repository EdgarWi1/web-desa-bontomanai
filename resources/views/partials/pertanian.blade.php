<div class="indent-8 md:indent-16 font-bold">
        C. Perawatan Tanaman
</div>
<div class="indent-8 md:indent-16">
        Perawatan pertama dilakukan saat beruisa 12 hari (2 minggu) dengan diberikan pupuk yaitu ZA, Urea, KCL, dan SP3,
        dengan perbandingan 3 Urea : 1 ZA :( 1)⁄2 KCL : :( 1)⁄2 SP3, yang dicampur jadi satu pemberian pupuk ini
        sempurna bagi petani yang memiliki keuangan yang lebih, tetapi yang tidak hanya menggunakan Urea dan ZA atau
        bahkan Urea atau ZA saja.
        <br>
        <br>
</div>
<div class="indent-8 md:indent-16">
        Pemupukan ini dinamakan pemupukan dasar dengan tujuan agar tanaman tampak hijau subur dan batang tanaman besar.
        Memasuki usia 1 bulan, tanaman sudah menunjukkan tanda-tanda akan berbunga dan pada saat itulah dilaakukaan
        pemupukan kedua dengan tujuan agar buah tanaman besar. Kemudian perawatan selanjutnya ketika sudah berusia 25
        hari dilakukan penyemprotan rumput yang menggunakan racun Gramoxon dengan pertimbangan petani bahwa racun jenis
        ini dianggap cukup bagus, karenaa sekalipun tamanan ada yang terkena percikan air semprotan tidak berpengaruh
        terhadap tanaman, sehingga petaani lebih banyak menggunakan racun tersebut untuk pembersihan rumput disekitar
        tanaman.
