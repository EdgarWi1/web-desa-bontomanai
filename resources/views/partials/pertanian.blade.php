<div class="indent-8 md:indent-16 font-bold">
        A. Tanaman Pangan Dan Hortikultura
</div>
<div class="indent-8 md:indent-16">
        Jenis tanaman pangan utama yang dibudidayakan petani Desa Bontomanai adalah jagung, padi dan cabai, sementara
        jenis tanaman Hortikultura yang dibudidayakan petani adalah jenis kacang-kacangan, tomat dan jenis buah lainnya
        meskipun dalam jumlah ynag tidak banyak, karena selain faktor lahan juga karena air untuk menyiram cukup
        terbatas, khususnya pada musim kemarau.
