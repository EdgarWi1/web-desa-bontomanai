        <br>
</div>
<div class="indent-8 md:indent-16 font-bold">
        D. Pemanenan dan Penanganan Pasca Panen
</div>
<div class="indent-8 md:indent-16">
        Seperti biasanya ketika usia jagung kuning sudah mencapai 3 bulan (100 hari ) maka dilakukan pemanenan. Biasanya
        hasil panen dikupas dan dibersihkan dikebun sehingga sampai dirumah sudah siap dros itupun jika musim kemarau.
        Tetapi jika musim hujan maka hasil tanaman jagung diangkat kerumah kemudia dijemur dan setelah kering barudi
        dros (dikeluarkan dari batangan) pengangkutan dari kebun biasanya menggunakan tenaga kuda.
</div>
