        dengan pembersihan dari rumput-rumput kecil dan rumput tua yang belum sempat dibersihkan pada saat habis panen
        tahun sebelumnya.muntuk lahan kedua biasanya menggunakan dua cara pembersihan lahan, bagi petani yang memiliki
        modal yang memadai mereka menggunakan racun untuk membasmi rumput dengan memakai jenis racun seperti prima up
        dan supremo ataupun jenis racun lain yang berfungsi untuk pengelolah lahan. Adapun untuk petani yang memiliki
        sedikit modal, biasanya mereka melakukan pembersihan lahan dengan cara membersihkan dan mengumpulkan rumput
        kemudian mereka membakar. Namun kebiasaan ini oleh sebagian petani kurang disadari bahwa hal ini bisa
        menyebabkan hilang dan berkurangnya kesuburan tanah akibat pembakaran rumput yang dilakukan.
        Selain itu penyiapan lahan juga biasanya menggunakan tenaga kuda bagi yang memiliki ternak kuda sedangkan yang
        memiliki kemampuan finansial biasanya merekaa membajak sawah dengan mengguanakan handtraktor meskipun harus
        menyewa.
