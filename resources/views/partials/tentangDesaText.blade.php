        Desa Bontomanai adalah salah satu Desa yang asal mula kelahirannya adalah hasil pemekaran dari desa kalimporo (
        sebuah Desa yang terletak disebalah barat desa Bontomani Desa yang asal mula bernama Mangngalero, namun diubah
        menjadi Desa Bontomanai yang artinya gunung “menonjol”. nama ini di ambil karena Desa ini memiliki gunung yang
        bermunculan sehingga di namakan Desa Bontomanai. Kepala Desa yang menjabat pada saat itu adalah bapak Alimajjid.
        SOS masa hidmad 2005 sampe 2006 dan tidak lama kemudian diangkat menjadi lura. dikarenakan adanya kekosongan
        jabatan maka Bapak Abdul Rahman. sos selaku Ketua bpd (badan permusyawaratan daerah) pada saat itu di tunjuk
        langsung sebagai pelaksana tugas oleh kabupaten, 2 tahun kemudian diadakan pemilihan secara langsung untuk
        memili keopala desa yang baru dan bapak saat itu adalah bapak saparuddin. setelah pemilian terjadi maka terpilih
        bapak abdul. rahman sebagai kepala desa berikutnya. luas wilayah desa bontomanai di perkirakan mencapai 1,65 m
        dengan wilaya pemerintahannya meliputi 8 dusun yakni : <br>
