        1. Badan permusyawaratan Desa (BPD)
</div>
<div class="indent-8 md:indent-16">
        Secara structural pengurus BPD desa bontomanai terdiri dari seorang ketua yang berlatar belakang pendidikan SLTA
        dengan jumlah anggota 9 orang dengan tingkat pendidikan SLTP dan SLTA apa yang di lakukan slama ini adalah
        melakukan musyawarah dengan pemerintah desa dalam pembangunan desa , sebagai panitia dalam pemelihan kepala
        desa, namun BPD tersebut belum mampu melaksanakan tugas secara maksimal akibat keterbatasan prasarana penunjang
        dan belum tersediahnya kantor yang permanen.
        <br>
        <br>
</div>
<div class="indent-8 md:indent-16 font-bold">
        2. Pemberdayaan Dan Kesehjahteraan Keluarga (PKK)
</div>
<div class="indent-8 md:indent-16">
        Pengurus kelompok pkk di desa bontomanai terdiri satu ketua, satu sekretaris,dan satu bendahara. Kelompok ini
        memiliki dan fungsi ysng semuanya bertujuan menndorong dan meningkatkan keberdayaan dan keseejahteraan keluarga
        dan masyarakaat. Sehubungan dengan tugas dan fungsinystersebut ngurus kelompok ini seharusnya lebih insentif
        melakukan kegiatan di desa bberupa penyuluhan kesehatan, penanaman tanaman obat di pekarangan namun pada
        kenyataannya belum sesuai yang di harapkanoleh masyarakat desa.
        <br>
        <br>
</div>
<div class="indent-8 md:indent-16 font-bold">
        3. Remaja Masjid
</div>
<div class="indent-8 md:indent-16">
        Remaja masjid didesa Bontomanai ini melakukan pembinaan TK-TPA di setiap masjid di 8 dusun, sebagai bentuk
        peningkatan pendidikan masyarakat yang diharapkan membawa dampak positif dalam kehidupan masyarakat . Antusias
        masyarakat dengan lembaga ini sangat tinggi seiring dengan perencanaan pemberantasan buta huruf Al-Qurâ€™an. Lewat
        lembaga ini telah banyak manfaat dirasakan masyarakat dalam rangka mencapai kebahagiaan dunia-akhirat. Namu
        demikian pihak pengelolah merasa masih kekurangan dalam hal buku iqro dan para pengurusnya hanya bekerja secara
        suka rela, sehingga pihak pemerintah dan Departemen agama perlu memperhatikan secara serius.
        <br>
        <br>
</div>
<div class="indent-8 md:indent-16 font-bold">
        4. Karang Taruna
</div>
<div class="indent-8 md:indent-16">
        Berdiri pada tahun 2007(desa persiapan) yang dirintis oleh Daswang Dkk. Ketua pertama yaitu Muhammad Rudi,S.pd,
        ketua kedua yaitu Adriadi (2018) dan ketua sekarang yaitu Musliadi, beranggotakan 23 orang. Lembaga ini
        merupakan wadah bagi genrasi muda untuk melakukan aktifitas kepemudaan,kegiatan olahraga dan ikut serta dalam
        pembangunan desa, namun lembaga ini belum mampu secara maksimal melaksanakan kegiatan-kegiatan akibat
        keterbatasan sarana dan prasarana.
        <br>
        <br>
</div>
<div class="indent-8 md:indent-16 font-bold">
        5. Kelompok Tani
</div>
<div class="indent-8 md:indent-16">
        Terbentuk pada tahun 2007 dan diresmikan oleh Dinas pertanian (dalam kondisi desa persiapan). Terdapat 22
        kelompok tani yang beranggotakan 25-30 orang tiap kelompok. Kegiatan selama ini adalah masih dalam usaha
        pertanian, dimana kelompok tani menyiapkan kebutuhan bagi petani dan permodalan bagi usaha produktif laiinnya.
        Untuk memenuhi kebutuhan yang masih sangat kurang sehingga perlu penambahan modal sekaligus bekerja sama dengan
        pemerintah setempat untuk mengadakan penagihan bagi anggota yang meminjam dan belum mengembalikan, kurangnya
        faktor kesadaran dikalangan anggota kelompok tani tersebut.
        <br>
        <br>
</div>
