<div class="indent-8 md:indent-16">
        Penduduk Desa Bontomanai tidak terlalu jauh berbeda dengan penduduk desa lainnya selain ulet dan juga rajin
        bekerja terutama pada sektor pertanian dan perkebunan sebagai sumber mata pencaharian. Begitu rajin dan giatnya
        masyarakat lebih mementingkan pekerjaannya ketimbang menhadiri acara pertemuan/rapat di kantor desa atau
        kegiatan-kegiatan desa sehingga kelihatan bahwa partisipasi masyarakat sudah mulai berkurang dalam kegiatan
        pembangunan bahkan budaya gorong royong untuk lkegiatan pembangun desa sudah mulai menipis akan tetapi khusus
        pada kegiatan kekeluargaan itu masi kental pada diri warga seperti pada acara perkawinan dan akidah dll. <br>
</div>

<div class="indent-16">
        Berdasarkan hasil pekerjaan yang di lakukan oleh masyarakat sepanjang hari dan sepanjang tahun akan sangat
        berpengaruh pula pada tingkat kehidupan sehari-hari, sehingga dengan jelas terlihat perbedaan dari setiap
        keluarga. Ini terbukti bahwa dari jumlah masyarakat melakukan aktifitas bertani cukup banyak dan mereka hidup
        dari apa yang mereka hasilkan melalaui kegiatan dan usaha pertanian. Keadaan ini seacara umum hampir sama dengan
        desa-desa lain yang ada di wilayah kabupaten jeneponto .
</div>
